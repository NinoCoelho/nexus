"""generate_image tool — text-to-image and image-edit via OpenAI / Gemini.

The tool resolves API keys from the existing provider configs ("openai"
and "google-gemini") using the same precedence the LLM provider registry
uses (``credential_ref`` → ``use_inline_key`` → ``api_key_env``). It
calls the matching client in :mod:`nexus.agent.image_gen`, persists the
generated bytes under ``assets/generated/`` in the vault, and returns a
markdown ``![]()`` snippet the agent can paste straight into its reply
— which the UI's ``MarkdownView`` renders inline thanks to the
``vault://`` → ``/vault/raw`` rewrite.

Same API key, no extra org verification required for either provider.
"""

